    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, OperatingSystem $operating)
    {
        $operating->update($request->all());
        return redirect()->route('operatings.index');
