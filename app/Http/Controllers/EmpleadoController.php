    public function searchEmpleado(Request $request)
    {
        $rules = [
            'nombre' => 'required|string',
            'tipo' => 'notin:0'
        ];
        $messages = [
            'nombre.required' => 'Debe introducir un texto',
            'nombre.string' => 'Cadena no valida',
            'tipo.notin' => 'Debe elegir un tipo de campo a buscar',
        ];
        $this->validate($request, $rules, $messages);

        $resultado = Empleado::buscarpor($request->tipo, $request->nombre)->active()->paginate(15);

        if ($resultado->count() > 0) {
            return view('empleados.index', compact('resultado'));
        }
        return redirect()->route('empleados.index')->with('success', "No hay resultados que coincidan");
    }
